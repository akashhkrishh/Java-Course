class Variable {
    public static void main(String a[]){
        
        int num1 = 1230443;
        short num2 = 1243;
        byte b = 127;
        long l = 12123334l;

        char c = 'A';
        boolean H = true;

        float f = 12.5f;
        double d = 12.5;
        

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(b);
        System.out.println(l);
        System.out.println(c);
        System.out.println(H);
        System.out.println(f);
        System.out.println(d);
    }    
}
