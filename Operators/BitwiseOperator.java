class BitwiseOperator{
    public static void main(String arg[]){
        
        System.out.println("\nBitwise Operators :");
        int a = 10;
        int b = 20;
        int c ;
        System.out.println("\nA = "+a+" B = "+b);
        c = a^b ; 
        System.out.println("\nA^B : "+c);
        c = a&b ; 
        System.out.println("\nA&B : "+c);
        c = a|b ; 
        System.out.println("\nA|B : "+c);
        
    }
}
