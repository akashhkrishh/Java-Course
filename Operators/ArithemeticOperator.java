class ArithemeticOperator{
    public static void main(String arg[]){
        
        System.out.println("\nArithemetic Operators :");

        int a = 10;
        int b = 2;

        int add = a + b;
        int sub = a - b;
        int mul = a * b;
        float div = a / b;
        int mod = a % b;

        System.out.println("\nAddition : "+add);  
        System.out.println("Subraction : "+sub);  
        System.out.println("Multiplication : "+mul);  
        System.out.println("Division : "+div);  
        System.out.println("Modulo Division : "+mod); 
        
    }
}
